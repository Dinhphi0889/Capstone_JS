function Product(_id, _name, _price, _screen, _backCam, _frontCam, _image, _description, _type) {
    this.id = _id;
    this.name = _name;
    this.screen = _screen;
    this.backCamera = _backCam;
    this.frontCamera = _frontCam;
    this.price = _price;
    this.img = _image;
    this.desc = _description;
    this.type = _type;
}